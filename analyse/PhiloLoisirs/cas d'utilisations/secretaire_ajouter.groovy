Acteur : sécrétaire
But  : ajouter une ou plusieurs activites dans le  catalogue .
Pré-condition :   existance d'un catalogue d'activites
Post-condition : augmentation de l'offre des activités disponibles 
 sur le site.
Scénario principale : //Etape/Acteur/Description de l'action
                          1  /secretaire/consulter le catlogue d'activités
                          2/ secretaire/constat de l'absence de l' activite
                          3/secretaire/validation de l' activité a rajouté
                          4/secretaire/ajout de celle-ci.


 Scenari alternatif : alternative 2a activite deja existante  
                     1/pas de besoin de rajouter une activite

                     alternative 3a activite non valide
