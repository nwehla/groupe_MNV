Use case : créer
Acteur principal : secrétaire
Acteur secondaire : aucun
Objectif : créer un catalogue d'activités
Pré-condition : vouloir proposer des activités aux adhérents du club
Poste-condition : disposer une liste d'activités pour les adhérents
Déclencheur : des demandes venant des adhérents du club
Sénari : 
1. créer une base de données,
2. choisir les activités, 
3. les saisir,
4. les enregistre dans la base de données