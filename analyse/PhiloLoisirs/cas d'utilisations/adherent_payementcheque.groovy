Use case : regler
Acteur : adhérent
But : payer les frais des activités par chèque 
Pré-condition : dispose d'un chéquier valide et que le compte de l'adherent 
                 soit suffisament alimenté.
Post-condition: pouvoir pratiquer ses activites
Scénario: Etape/acteur/Description de l'action
          1/ adhérent /choisit le payement par chéque
          2/systeme/ demande s'il regle en une seule fois ou non
          3/adherent/ choisit .
          4/ systeme/affiche le recaputilatif de son motant à payer et 
                    les coordonnées du club ,pour expédition du chéque
                     
Scenari alternatif : alternatif 3a l'adherent choisit de payer en plusieurs fois
                                 4a le  lui affiche les echéanciers possibles
                                 5  le client choisit 
                                 6  le systeme lui affiche la confirmation de son choix
                                    et les coordonnées du club pour expédition du chéque
                                    avec un lien retour au club    
                                     
                        
                        
                                     